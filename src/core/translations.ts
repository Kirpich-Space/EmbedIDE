const TRANSLATIONS: Record<LangCode, DeepRecord> = { en, ru, zh, ja, de, fr }
const FLAT_CACHE = new Map<LangCode, Record<string, string>>()

export function getFlatTranslations(lang: LangCode): Record<string, string> {
  let cached = FLAT_CACHE.get(lang)
  if (!cached) {
    cached = flatten(TRANSLATIONS[lang] || en)
    FLAT_CACHE.set(lang, cached)
  }
  return cached
}

